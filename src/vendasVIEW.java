    private void blnListarVendidosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_blnListarVendidosActionPerformed
        ArrayList<ProdutosDTO> produtosVendidos = new ProdutosDAO().listarProdutosVendidos();

        // Cria um modelo de tabela para armazenar os dados dos produtos vendidos
        DefaultTableModel model = new DefaultTableModel();

        // Define as colunas da tabela
        model.addColumn("ID");
        model.addColumn("Nome");
        model.addColumn("Valor");
        model.addColumn("Status");

        // Adiciona os produtos vendidos ao modelo de tabela
        for (ProdutosDTO produto : produtosVendidos) {
            model.addRow(new Object[]{produto.getId(), produto.getNome(), produto.getValor(), produto.getStatus()});
        }

        // Define o modelo de tabela para a tabela listaProdutos
        listaProdutos.setModel(model);
    }//GEN-LAST:event_blnListarVendidosActionPerformed

